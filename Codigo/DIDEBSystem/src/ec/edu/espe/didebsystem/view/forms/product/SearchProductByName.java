    private void listActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_listActionPerformed
        if(!this.list.getSelectedItem().toString().equals("Seleccione el Nombre del Producto")){
            ProductDAO DAO = ProductDAO.getInstance();
            Product product = DAO.searchProductByName(this.list.getSelectedItem().toString());

            this.txtPriceC.setText(product.getPriceSell());
            this.txtPriceV.setText(product.getPricePurchase());
            this.txtDescription.setText(product.getDescription());
        } else {
            emptyFields();
        }
    }//GEN-LAST:event_listActionPerformed

